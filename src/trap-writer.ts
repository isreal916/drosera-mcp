// ── Main export ────────────────────────────────────────────────────────────

export function generateTrap(input: TrapInput): GeneratedTrap {
  const {
    description,
    contractAddress = "0x0000000000000000000000000000000000000000",
    blockSampleSize = 2,
  } = input;

  const pattern = detectPattern(description);
  const contractName = buildContractName(pattern, description);
  const addr = contractAddress;

  const warnings: string[] = [];
  if (contractAddress === "0x0000000000000000000000000000000000000000") {
    warnings.push("Set the TARGET contract address before deploying.");
  }
  warnings.push("Set response_contract and response_function in drosera.toml.");
  warnings.push("Run `forge build` to compile, then `drosera test` to validate.");

  let solidityCode: string;
  let explanation: string;

  switch (pattern) {
    case "ownership":
      solidityCode = templateOwnership(contractName, addr);
      explanation = `Monitors the owner() of ${addr} every block. Triggers if the owner changes from EXPECTED_OWNER. Set EXPECTED_OWNER to the legitimate owner address before deploying.`;
      warnings.push("Set EXPECTED_OWNER to the legitimate owner address in the contract.");
      break;

    case "balance":
      solidityCode = templateBalance(contractName, addr);
      explanation = `Monitors the ETH balance of ${addr} every block. Triggers if the balance drops below THRESHOLD (default 1 ETH). Adjust THRESHOLD to your needs.`;
      warnings.push("Set THRESHOLD to your minimum acceptable ETH balance (in wei).");
      break;

    case "erc20supply":
      solidityCode = templateErc20Supply(contractName, addr);
      explanation = `Monitors the totalSupply() of the ERC20 token at ${addr}. Triggers if supply changes by more than CHANGE_THRESHOLD_BPS basis points in a single block. Requires block_sample_size >= 2.`;
      warnings.push("Adjust CHANGE_THRESHOLD_BPS (default 1000 = 10%).");
      break;

    case "erc20balance":
      solidityCode = templateErc20Balance(contractName, addr);
      explanation = `Monitors the ERC20 token balance of WATCHED_ADDRESS for the token at ${addr}. Triggers if the balance drops below MIN_BALANCE.`;
      warnings.push("Set WATCHED_ADDRESS to the address you want to monitor.");
      warnings.push("Set MIN_BALANCE to the minimum acceptable token balance.");
      break;

    case "paused":
      solidityCode = templatePaused(contractName, addr);
      explanation = `Monitors the paused() state of the protocol at ${addr}. Triggers the moment the protocol becomes paused — useful for detecting emergency shutdowns or exploits.`;
      break;

    case "threshold":
      solidityCode = templateThreshold(contractName, addr);
      explanation = `Monitors a uint256 value from ${addr} every block. Triggers if the value drops below THRESHOLD. Replace getValue() with the actual function you want to monitor.`;
      warnings.push("Replace getValue() with the actual function name from your target contract.");
      warnings.push("Set THRESHOLD to the minimum acceptable value.");
      break;

    case "drop":
      solidityCode = templateDrop(contractName, addr);
      explanation = `Monitors a value from ${addr} for sudden percentage drops across blocks. Triggers if the value drops by more than DROP_THRESHOLD_BPS basis points in a single block. Requires block_sample_size >= 2.`;
      warnings.push("Replace getValue() with the actual function name.");
      warnings.push("Adjust DROP_THRESHOLD_BPS (default 2000 = 20%).");
      break;

    default:
      solidityCode = templateCustom(contractName, addr, description);
      explanation = `Generic trap template for: "${description}". The collect() and shouldRespond() functions have clear TODO comments — fill them in based on what you want to monitor.`;
      warnings.push("This is a generic template. Implement collect() and shouldRespond() for your use case.");
      break;
  }

  const suggestedToml = buildToml(contractName, blockSampleSize, contractAddress);

  return {
    contractName,
    pattern,
    solidityCode,
    explanation,
    suggestedToml,
    warnings,
  };
}