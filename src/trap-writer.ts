/**
 * trap-writer.ts
 *
 * Template-based Drosera Trap generator.
 * No API calls, no API key, no external dependencies.
 *
 * How it works:
 *  1. Detect which trap PATTERN fits the description (keyword matching)
 *  2. Fill the matching template with user-provided values
 *  3. Return valid, compilable Solidity + drosera.toml
 *
 * Templates:
 *  - ownership    → monitors if owner/admin address changes
 *  - balance      → monitors ETH balance drops below threshold
 *  - erc20supply  → monitors ERC20 totalSupply changes
 *  - erc20balance → monitors ERC20 token balance of an address
 *  - paused       → monitors if a protocol's paused state changes
 *  - threshold    → monitors any uint256 value against a threshold
 *  - drop         → monitors a value for a sudden % drop across blocks
 *  - custom       → generic fallback template with clear TODOs
 */

// ── Types ──────────────────────────────────────────────────────────────────

export interface TrapInput {
  description: string;
  contractAddress?: string;
  contractAbi?: string;
  blockSampleSize?: number;
}

export interface GeneratedTrap {
  contractName: string;
  pattern: string;
  solidityCode: string;
  explanation: string;
  suggestedToml: string;
  warnings: string[];
}

// ── Pattern detection ──────────────────────────────────────────────────────

type PatternKey = "ownership" | "balance" | "erc20supply" | "erc20balance" | "paused" | "threshold" | "drop" | "custom";

function detectPattern(desc: string): PatternKey {
  const d = desc.toLowerCase();
  if (/owner|admin|access control|role|signer/.test(d))     return "ownership";
  if (/paused|pause|unpause|frozen|freeze/.test(d))          return "paused";
  if (/total.?supply|mint|burn|supply.change/.test(d))       return "erc20supply";
  if (/erc20|token.balance|token.amount/.test(d))            return "erc20balance";
  if (/eth.balance|native.balance|ether.balance/.test(d))    return "balance";
  if (/drop|decrease|fall|crash|sudden|spike/.test(d))       return "drop";
  if (/threshold|below|above|exceed|limit|cap/.test(d))      return "threshold";
  return "custom";
}

// ── Name generator ─────────────────────────────────────────────────────────

function buildContractName(pattern: PatternKey, desc: string): string {
  const words = desc
    .replace(/[^a-zA-Z0-9 ]/g, "")
    .split(" ")
    .filter(Boolean)
    .slice(0, 3)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase());

  const suffix = "Trap";
  return words.join("") + suffix;
}

function toSnakeCase(str: string): string {
  return str
    .replace(/([A-Z])/g, "_$1")
    .toLowerCase()
    .replace(/^_/, "");
}

// ── TOML builder ───────────────────────────────────────────────────────────

function buildToml(contractName: string, blockSampleSize: number, contractAddress?: string): string {
  return [
    `# drosera.toml — generated by drosera-mcp-server`,
    `# Fill in eth_rpc_url, response_contract, and response_function before deploying.`,
    ``,
    `[drosera]`,
    `eth_rpc_url = "https://rpc.hoodi.ethpandaops.io"  # replace with your RPC`,
    ``,
    `[traps.${toSnakeCase(contractName)}]`,
    `path = "out/${contractName}.sol/${contractName}.json"`,
    `response_contract = "${contractAddress ?? "0xYOUR_RESPONSE_CONTRACT"}"`,
    `response_function = "YOUR_RESPONSE_FUNCTION()"`,
    `block_sample_size = ${blockSampleSize}`,
    `min_number_of_operators = 1`,
    `max_number_of_operators = 5`,
    `private_trap = false`,
  ].join("\n");
}

// ── TEMPLATES ──────────────────────────────────────────────────────────────

function templateOwnership(name: string, address: string): string {
  return `// SPDX-License-Identifier: UNLICENSED
pragma solidity ^0.8.20;

import "drosera-contracts/interfaces/ITrap.sol";

interface IOwnable {
    function owner() external view returns (address);
}

/// @title ${name}
/// @notice Monitors the owner() of a contract and triggers if it changes.
/// @dev Implements the Drosera ITrap interface.
contract ${name} is ITrap {

    /// @notice The contract whose ownership is being monitored.
    address public constant TARGET = ${address};

    /// @notice The expected owner address. Update this before deploying.
    address public constant EXPECTED_OWNER = 0x0000000000000000000000000000000000000000; // TODO: set expected owner

    struct Snapshot {
        address owner;
    }

    /// @inheritdoc ITrap
    /// @dev Reads the current owner every block.
    function collect() external view returns (bytes memory) {
        address currentOwner = IOwnable(TARGET).owner();
        return abi.encode(Snapshot({ owner: currentOwner }));
    }

    /// @inheritdoc ITrap
    /// @dev Triggers if the owner no longer matches EXPECTED_OWNER.
    function shouldRespond(bytes[] calldata data) external pure returns (bool, bytes memory) {
        if (data.length == 0) return (false, bytes(""));

        Snapshot memory latest = abi.decode(data[0], (Snapshot));

        if (latest.owner != EXPECTED_OWNER) {
            return (true, abi.encode("Owner changed unexpectedly", latest.owner));
        }

        return (false, bytes(""));
    }
}`;
}

