function templateErc20Supply(name: string, address: string): string {
  return `// SPDX-License-Identifier: UNLICENSED
pragma solidity ^0.8.20;

import "drosera-contracts/interfaces/ITrap.sol";

interface IERC20 {
    function totalSupply() external view returns (uint256);
}

/// @title ${name}
/// @notice Monitors the totalSupply of an ERC20 token.
/// @dev Triggers if supply changes by more than CHANGE_THRESHOLD_BPS basis points in one block.
contract ${name} is ITrap {

    /// @notice The ERC20 token contract to monitor.
    address public constant TOKEN = ${address};

    /// @notice Max allowed supply change in basis points (100 = 1%). Update before deploying.
    uint256 public constant CHANGE_THRESHOLD_BPS = 1000; // 10% — TODO: adjust

    struct Snapshot {
        uint256 totalSupply;
    }

    /// @inheritdoc ITrap
    function collect() external view returns (bytes memory) {
        return abi.encode(Snapshot({ totalSupply: IERC20(TOKEN).totalSupply() }));
    }

    /// @inheritdoc ITrap
    /// @dev Compares the two most recent snapshots and triggers on large supply changes.
    function shouldRespond(bytes[] calldata data) external pure returns (bool, bytes memory) {
        if (data.length < 2) return (false, bytes(""));

        Snapshot memory latest = abi.decode(data[0], (Snapshot));
        Snapshot memory prev   = abi.decode(data[1], (Snapshot));

        if (prev.totalSupply == 0) return (false, bytes(""));

        // Calculate absolute change in bps
        uint256 diff = latest.totalSupply > prev.totalSupply
            ? latest.totalSupply - prev.totalSupply
            : prev.totalSupply - latest.totalSupply;

        uint256 changeBps = (diff * 10_000) / prev.totalSupply;

        if (changeBps > CHANGE_THRESHOLD_BPS) {
            return (true, abi.encode("Total supply changed beyond threshold", changeBps));
        }

        return (false, bytes(""));
    }
}`;
}

